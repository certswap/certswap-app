    '.i18n-lang-btn:hover{color:#fff;border-color:rgba(255,255,255,.45);background:rgba(255,255,255,.08)}' +
    '.i18n-lang-btn.i18n-active{background:rgba(26,86,219,.45);color:#fff;border-color:rgba(26,86,219,.7)}';
  document.head.appendChild(style);

  /* ── Init ────────────────────────────────────────────────────────────────── */
  function init() {
    injectSwitcher();
    var lang = detectLanguage();
    _lang = lang;
    window._lang = lang;
    applyTranslations(lang);
  }

  window.t = t;
  window.setLanguage = setLanguage;

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
